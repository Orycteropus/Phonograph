package com.kabouzeid.gramophone.lyric.model;

public class Lyric {

    private String type;

    private String lyrics;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getLyrics() {
        return lyrics;
    }

    public void setLyrics(String lyrics) {
        this.lyrics = lyrics;
    }
}
